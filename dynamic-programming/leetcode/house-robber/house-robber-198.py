# T=n,S=n
def x(nums):
    n, dp = len(nums), {}

    def dfs(i):
        if i >= n:
            return 0
        if i in dp:
            return dp[i]
        dp[i] = max(nums[i] + dfs(i + 2), dfs(i + 1))
        return dp[i]

    return dfs(0)



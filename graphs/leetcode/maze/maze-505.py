from heapq import *


def outside(m, n, ri, ci):
    return ri in [-1, m] or ci in [-1, n]


def roll(grid, ri, ci, i, j):
    m, n = len(grid), len(grid[0])
    nri, nci, weight = ri, ci, 0
    while not outside(m, n, nri + i, nci + j) and grid[nri + i][nci + j] != 1:
        nri, nci = nri + i, nci + j
        weight += 1
    return nri, nci, weight


def bfs(grid, sri, sci, dri, dci):
    heap, vis = [], set()
    heappush(heap, (0, sri, sci))
    offsets = (-1, 0), (0, 1), (1, 0), (0, -1)
    while heap:
        weightSoFar, ri, ci = heappop(heap)
        if (ri, ci) in vis:
            continue
        vis.add((ri, ci))
        if (ri, ci) == (dri, dci):
            return weightSoFar
        for i, j in offsets:
            nri, nci, weight = roll(grid, ri, ci, i, j)
            heappush(heap, (weightSoFar + weight, nri, nci))
    return -1


